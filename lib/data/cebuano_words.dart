  /// Get words by category
  static List<CebuanoWord> getWordsByCategory(WordCategory category) {
    return words.where((word) => word.category == category).toList();
  }

  /// Get words by difficulty level
  static List<CebuanoWord> getWordsByDifficulty(int difficulty) {
    return words.where((word) => word.difficulty == difficulty).toList();
  }

  /// Get random word
  static CebuanoWord getRandomWord() {
    return words[(words.length * (DateTime.now().millisecond % words.length))];
  }

  /// Get random word by category
  static CebuanoWord getRandomWordByCategory(WordCategory category) {
    final categoryWords = getWordsByCategory(category);
    return categoryWords[(categoryWords.length *
        (DateTime.now().millisecond % categoryWords.length))];
  }

  /// Search words
  static List<CebuanoWord> searchWords(String query) {
    final lowerQuery = query.toLowerCase();
    return words
        .where(
          (word) =>
              word.cebuano.toLowerCase().contains(lowerQuery) ||
              word.english.toLowerCase().contains(lowerQuery),
        )
        .toList();
  }
