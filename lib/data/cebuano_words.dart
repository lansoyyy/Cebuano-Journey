/// Cebuano Words Database
/// Comprehensive collection of Cebuano words with translations and categories
class CebuanoWord {
  final String id;
  final String cebuano;
  final String english;
  final String pronunciation;
  final WordCategory category;
  final int difficulty; // 1-5 scale
  final String? exampleSentence;
  final String? exampleTranslation;

  const CebuanoWord({
    required this.id,
    required this.cebuano,
    required this.english,
    required this.pronunciation,
    required this.category,
    required this.difficulty,
    this.exampleSentence,
    this.exampleTranslation,
  });

  Map<String, dynamic> toJson() {
    return {
      'id': id,
      'cebuano': cebuano,
      'english': english,
      'pronunciation': pronunciation,
      'category': category.name,
      'difficulty': difficulty,
      'exampleSentence': exampleSentence,
      'exampleTranslation': exampleTranslation,
    };
  }

  factory CebuanoWord.fromJson(Map<String, dynamic> json) {
    return CebuanoWord(
      id: json['id'] as String,
      cebuano: json['cebuano'] as String,
      english: json['english'] as String,
      pronunciation: json['pronunciation'] as String,
      category: WordCategory.values.firstWhere(
        (e) => e.name == json['category'],
        orElse: () => WordCategory.general,
      ),
      difficulty: json['difficulty'] as int,
      exampleSentence: json['exampleSentence'] as String?,
      exampleTranslation: json['exampleTranslation'] as String?,
    );
  }
}

enum WordCategory {
  greetings,
  numbers,
  colors,
  animals,
  food,
  family,
  time,
  weather,
  emotions,
  actions,
  places,
  objects,
  general,
}

